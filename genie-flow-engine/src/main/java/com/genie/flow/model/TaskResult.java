package com.genie.flow.model;

public class TaskResult {

    private String result;

    private boolean directNext;

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public boolean isDirectNext() {
        return directNext;
    }

    public void setDirectNext(boolean directNext) {
        this.directNext = directNext;
    }
}
